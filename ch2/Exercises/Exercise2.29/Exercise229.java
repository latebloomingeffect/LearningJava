//Exercise 2.29
//Program inputs a character and outputs an integer representation 
//of a character

public class Exercise229
{
	//method main begins execution of Java application
	public static void main(String[] args)
	{
		System.out.printf("Integer representing %c is %d%n",'A',((int)'A'));
		System.out.printf("Integer representing %c is %d%n",'B',((int)'B'));
		System.out.printf("Integer representing %c is %d%n",'C',((int)'C'));
		System.out.printf("Integer representing %c is %d%n",'a',((int)'a'));
		System.out.printf("Integer representing %c is %d%n",'b',((int)'b'));
		System.out.printf("Integer representing %c is %d%n",'c',((int)'c'));
		System.out.printf("Integer representing %c is %d%n",'0',((int)'0'));
		System.out.printf("Integer representing %c is %d%n",'1',((int)'1'));
		System.out.printf("Integer representing %c is %d%n",'2',((int)'2'));
		System.out.printf("Integer representing %c is %d%n",'$',((int)'$'));
		System.out.printf("Integer representing %c is %d%n",'*',((int)'*'));
		System.out.printf("Integer representing %c is %d%n",'+',((int)'+'));
		System.out.printf("Integer representing %c is %d%n",'/',((int)'/'));
	}
}